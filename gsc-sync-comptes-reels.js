/**
 * GSC SYNC COMPTES RÉELS — Bouton admin "🔄 Synchroniser les comptes"
 * ──────────────────────────────────────────────────────────────────
 * Objectif : un seul geste qui force TOUS les compteurs (dashboard admin,
 * panneau Liaison Clubs, panneau "Synchronisation clubs") à se recalculer
 * depuis Firestore avec la même règle (computeUnifiedOrgCount), puis
 * signale à l'app publique (index.html, même origine GitHub Pages) de
 * ne pas utiliser son cache de 30s au prochain calcul.
 *
 * Ne remplace pas le temps réel existant (realtimeSync/onSnapshot reste
 * actif en continu) : ce bouton sert de garde-fou visuel + de purge de
 * cache immédiate quand on veut être sûr à 100% que ce qu'on voit dans
 * l'admin est bien ce que verra un visiteur qui charge l'app maintenant.
 */
/**
 * GSC SYNC ACTEURS SPORTIFS — Bouton admin "🔄 Synchroniser les acteurs sportifs"
 * ──────────────────────────────────────────────────────────────────
 * Deux problèmes réglés d'un même geste :
 *
 * 1) Les compteurs admin (dashboard, Liaison, panneau Synchronisation)
 *    peuvent être stales → on les recalcule tous depuis Firestore.
 *
 * 2) L'annuaire public (index.html → loadActors()) ne lit QUE la
 *    collection `users`. Une fiche `structuresSportives` (club/fédé/
 *    association/organisateur) sans compte `users` lié est donc
 *    invisible à l'accueil et dans l'annuaire, même si l'admin la
 *    compte correctement. Ce bouton crée, pour chaque fiche non
 *    reliée, un compte `users` minimal :
 *      - marqué `autoGenerated: true` et `claimed: false` pour qu'on
 *        sache qu'il s'agit d'un compte "vitrine" en attente d'un
 *        vrai responsable ;
 *      - relié via `structureId` (même convention que le reste de
 *        l'app — voir computeUnifiedOrgCount) ;
 *      - `status: 'active'` pour apparaître immédiatement à
 *        l'accueil/annuaire ;
 *      - modifiable tout de suite depuis l'onglet Comptes de l'admin
 *        (formulaire d'édition générique, comme n'importe quel autre
 *        compte), en attendant qu'un responsable réel le réclame.
 *    Idempotent : ne recrée jamais de compte pour une structure déjà
 *    reliée, même en cliquant plusieurs fois.
 */
(function () {
  'use strict';

  const FORCE_SYNC_KEY = 'gsc_force_resync_ts';
  const ORG_TYPE_TO_ROLE = { 'Club': 'club', 'Association': 'association', 'Fédération': 'federation', 'Organisateur': 'organisateur' };

  function injectButton() {
    const host = document.getElementById('liaison-content') || document.querySelector('.main-content') || document.body;
    if (!host || document.getElementById('gsc-sync-wrap')) return;

    const wrap = document.createElement('div');
    wrap.id = 'gsc-sync-wrap';
    wrap.className = 'gsc-sync-wrap';
    wrap.innerHTML = `
      <button id="gsc-sync-btn" class="gsc-sync-btn" type="button">
        <span class="gsc-sync-icon">🔄</span>
        <span class="gsc-sync-label">Synchroniser les acteurs sportifs</span>
      </button>
      <span id="gsc-sync-status" class="gsc-sync-status"></span>
    `;
    host.insertBefore(wrap, host.firstChild);

    document.getElementById('gsc-sync-btn').addEventListener('click', runSync);
  }

  function setStatus(text, kind) {
    const el = document.getElementById('gsc-sync-status');
    if (!el) return;
    el.textContent = text;
    el.className = 'gsc-sync-status' + (kind ? ' is-' + kind : '');
  }

  async function runSync() {
    const btn = document.getElementById('gsc-sync-btn');
    if (!btn || btn.disabled) return;
    if (!confirm('Ceci va créer un compte "acteur" pour chaque club/fédération/association/organisateur qui n\'en a pas encore, afin qu\'ils apparaissent à l\'accueil et dans l\'annuaire public. Continuer ?')) return;

    btn.disabled = true;
    btn.classList.add('is-loading');
    setStatus('Synchronisation en cours…');

    try {
      if (!window.firebase || !firebase.firestore) throw new Error('Firestore indisponible');
      const fs = firebase.firestore();

      // 1) Relecture fraîche, sans caches
      const [usersSnap, structSnap] = await Promise.all([
        fs.collection('users').get(),
        fs.collection('structuresSportives').get()
      ]);
      const users = usersSnap.docs.map(d => ({ id: d.id, ...d.data() }));
      const structures = structSnap.docs.map(d => ({ id: d.id, ...d.data() })).filter(s => s.status !== 'deleted');
      const linkedIds = new Set(users.map(u => u.structureId).filter(Boolean));

      // 2) Fiches sans compte, parmi les types reconnus comme "organisation"
      const missing = structures.filter(s => ORG_TYPE_TO_ROLE[s.type] && !linkedIds.has(s.id));

      // 3) Création en lot (idempotent : rien n'est fait pour une structure déjà liée)
      let created = 0;
      if (missing.length) {
        const batch = fs.batch();
        missing.forEach(s => {
          const ref = fs.collection('users').doc();
          batch.set(ref, {
            role: ORG_TYPE_TO_ROLE[s.type],
            nomOrganisation: s.nom || 'Sans nom',
            ville: s.ville || '',
            sport: s.discipline || '',
            telephone: s.telephone || '',
            structureId: s.id,
            status: 'active',
            autoGenerated: true,
            claimed: false,
            createdAt: new Date(),
            updatedAt: new Date(),
            source: 'gsc-sync-comptes-reels'
          });
          created++;
        });
        await batch.commit();
      }

      // 4) Recalcul + rafraîchissement de tous les affichages admin déjà en place
      const usersAfter = users.concat(); // les comptes créés seront repris au prochain recalcul temps réel
      let unifiedCount = null;
      if (typeof window.computeUnifiedOrgCount === 'function') {
        unifiedCount = await window.computeUnifiedOrgCount(usersAfter, ['club', 'federation', 'association']);
      }
      if (typeof window.loadDashboardStats === 'function') await window.loadDashboardStats();
      if (typeof window.gscRefreshClubsSyncPanel === 'function') window.gscRefreshClubsSyncPanel();

      // 5) Signale à l'app publique (même origine) de ne pas utiliser son cache de 30s
      try { localStorage.setItem(FORCE_SYNC_KEY, String(Date.now())); } catch (e) { /* tant pis */ }

      const stamp = new Date().toLocaleTimeString('fr-FR');
      const detail = created > 0
        ? `${created} compte(s) créé(s) · ${structures.length} fiches · ${linkedIds.size + created} liées au total`
        : `Rien à créer — toutes les fiches ont déjà un compte (${structures.length} fiches, ${linkedIds.size} liées)`;
      setStatus(`✅ Synchronisé à ${stamp} — ${detail}`, 'ok');
      if (created > 0 && typeof window.toast === 'function') {
        window.toast(`${created} compte(s) créé(s) — visibles dans l'annuaire, modifiables dans l'onglet Comptes.`, 'success');
      }
    } catch (err) {
      console.error('[GSC SYNC] Erreur:', err);
      setStatus('❌ Échec de la synchronisation — voir console (F12)', 'error');
    } finally {
      btn.disabled = false;
      btn.classList.remove('is-loading');
    }
  }

  function init() {
    injectButton();
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

  console.log('[GSC SYNC] Bouton de synchronisation des acteurs sportifs chargé');
})();
